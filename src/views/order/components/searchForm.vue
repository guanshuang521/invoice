<script>
// import { mapGetters } from 'vuex'
/**
 * search条件
 * html使用全局组件： <search-Form v-bind:page="home" v-bind:share-massage="shareMassage" v-bind:other="other" v-bind:isloading="true" v-on:click="close"></search-Form>
 * 传入参数：
 * moudelType 字符串 区分不同页面
 * queryConditionsForm 对象格式  表单内容
 * {
 *      content:'text',  描述 可不传
 *      url:''，  图片地址 必传
 * }
 *region 数组类型  订单状态下拉框选项
 *other 对象 其他补充信息，可自行添加
 *补充信息：查询方法在父组件中写命名为searchSubmit
 */
export default {
  name: 'SearchForm',
  props: {
    moudelType: {
      type: String,
      default: ''
    },
    config: {
      type: Array,
      default: () => []
    },
    data: {
      type: Object,
      default: () => {}
    }
  },
  mounted() {
    this._copy = Object.assign({}, this.data)
  },
  methods: {
    onSubmit() {
      this.$parent.searchSubmit()
    },
    reset(data) {
      this.$nextTick(() => {
        this.$refs[data].resetFields()
        // this.$emit('update:data', Object.assign({}, this._copy))
        // this.onSubmit()
        this.$message({
          message: '查询条件重置成功',
          type: 'success'
        })
      })
    }
  }
}
</script>
